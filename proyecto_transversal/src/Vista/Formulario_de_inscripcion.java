    private void jbInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbInscribirActionPerformed
        // TODO add your handling code here:
        AlumnoData aData = new AlumnoData();
        String dni = jcAlumnos.getSelectedItem().toString();
        Alumno alumno = aData.buscarPorDni(Integer.parseInt(dni.split(",")[0]));
        //Crea una materia en base a una materia seleccionada en la tabla
        Materia materia = new Materia();
        int seleccionado = jtMaterias.getSelectedRow();
        materia.setAnioMateria(Integer.parseInt(tModel.getValueAt(seleccionado, 2).toString()));
        materia.setIdMateria(Integer.parseInt(tModel.getValueAt(seleccionado, 0).toString()));
        materia.setNombre(tModel.getValueAt(seleccionado, 1).toString());
        //Crea una inscripcion nueva
        if(jrMateriasI.isSelected()){
            InscripcionData iData = new InscripcionData();
            Inscripcion inscripcion = new Inscripcion();
            inscripcion.setAlumno(alumno);
            inscripcion.setMateria(materia);
            inscripcion.setNota(0);
            iData.guardarInscripcion(inscripcion);
        }else{
            JOptionPane.showMessageDialog(null,"Debe situarse en la tabla de materias no inscriptas");
        }
    }//GEN-LAST:event_jbInscribirActionPerformed

